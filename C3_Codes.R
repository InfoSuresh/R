

##Conversion to numeric
#Interger to numeric
int_var_name <- as.integer(23)
class(int_var_name)
print(int_var_name)
int_num_var_name <- as.numeric(int_var_name)
class(int_num_var_name)
print(int_num_var_name)

#Complex to numeric
comp_var_name <- 2+3i
class(comp_var_name)
print(comp_var_name)
comp_num_var_name <- as.numeric(comp_var_name)
class(comp_num_var_name)
print(comp_num_var_name)

#Logical to numeric
log_var_name <- TRUE
class(log_var_name)
print(log_var_name)
log_num_var_name <- as.numeric(log_var_name)
class(log_num_var_name)
print(log_num_var_name)

